export function getRandomTarotReading(cardName: string, category: string): { card: TarotCardInfo; readingText: string } {
  const card = tarotCards.find(c => c.name.includes(cardName) || cardName.includes(c.name)) || tarotCards[0];
  
  let readingText = card.generalMeaning;
  if (category === "연애" || category === "하루타로 - 상대방 마음" || category === "하루궁합") {
    readingText = card.loveMeaning;
  } else if (category === "진로" || category === "하루진로") {
    readingText = card.careerMeaning;
  }
  
  return { card, readingText };
}
